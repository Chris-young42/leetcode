/**
 * 爬楼梯问题（LeetCode 70）
 * 题意：你要爬 n 阶楼梯，每次可以爬 1 或 2 阶，
 * 问有多少种不同的方法可以爬到楼顶。
 *
 * 思路：动态规划（类似斐波那契数列）
 * - 若只有 1 阶：只有 1 种方法（一次爬 1 阶）
 * - 若有 2 阶：有 2 种方法（1+1 或直接 2）
 * - 若超过 2 阶：第 n 阶的走法 = 到第 (n-1) 阶的走法 + 到第 (n-2) 阶的走法
 *   因为你到第 n 阶，要么是从 (n-1) 阶爬 1 阶，要么从 (n-2) 阶爬 2 阶
 *
 * 公式：f(n) = f(n - 1) + f(n - 2)
 *
 * @param {number} n - 楼梯的阶数
 * @return {number} - 到达第 n 阶的不同方法数
 */
var climbStairs = function (n) {
  // 边界条件：
  // 如果只有 1 阶，只有 1 种走法；
  // 如果有 2 阶，有 2 种走法（1+1 或 2）
  if (n <= 2) return n;

  // 初始化前两个状态：
  // a 表示 f(n-2)，b 表示 f(n-1)
  let a = 1, // f(1)
      b = 2; // f(2)

  // 从第 3 阶开始，逐步计算到第 n 阶
  // 因为前两个已知，所以循环 n-2 次即可
  for (let i = 3; i <= n; i++) {
    // 当前台阶的方法数 = 前一阶 + 前两阶
    let temp = a + b;

    // 状态前移：
    // 把 b（前一阶）变成下一轮的前两阶；
    // 把 temp（当前阶）变成下一轮的前一阶。
    a = b;
    b = temp;
  }

  // 循环结束后，b 就是到达第 n 阶的走法数
  return b;
};
